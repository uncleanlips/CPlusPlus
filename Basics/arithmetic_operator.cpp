#include <iostream>
using namespace std;

int main() {
  	int num1 = 240;
  	int num2 = 40;
  	
	cout << "num1 + num2: " <<(num1 + num2) << endl;
  	cout << "num1 - num2: " <<(num1 - num2) << endl;
  	cout << "num1 * num2: " <<(num1 * num2) << endl;
  	cout << "num1 / num2: " <<(num1 / num2) << endl;
  	cout << "num1 % num2: " <<(num1 % num2) << endl;
  	
	return 0;
}
